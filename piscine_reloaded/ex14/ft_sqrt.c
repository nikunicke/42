int		ft_pow(int nb)
{
	return (nb * nb);
}

int		ft_sqrt(int nb)
{
	int		i;

	i = 0;
	while (i <= (nb + 1) / 2)
	{
		if (nb == ft_pow(i))
			return (i);
		i++;
	}
	return (0);
}
